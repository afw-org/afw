/**
 * Typescript interface definition for propTypes
 */
export interface ICheckboxProps {
    /**
     * boxSide
     * Data Type: (string)
     * 
     * Specifies which side the checkbox is on (start or end).
     */
    boxSide?:                           string;
    /**
     * description
     * Data Type: (string)
     * 
     * The description for the Checkbox component.
     */
    description?:                       string;
    /**
     * disabled
     * Data Type: (boolean)
     * 
     * Specifies whether the Checkbox component is disabled.
     */
    disabled?:                          boolean;
    /**
     * label
     * Data Type: (string)
     * 
     * The label for the Checkbox component.
     */
    label?:                             string;
    /**
     * value
     * Data Type: (boolean)
     * 
     * The value for the Checkbox component.
     */
    value?:                             boolean;
}

