/**
 * Typescript interface definition for propTypes
 */
export interface ILinkProps {
    /**
     * disabled
     * Data Type: (boolean)
     * 
     * Specifies whether the the Link is disabled.
     */
    disabled?:                          boolean;
    /**
     * text
     * Data Type: (string)
     * 
     * The text value displayed for the Link component.
     */
    text?:                              string;
    /**
     * url
     * Data Type: (string)
     * 
     * The URL for the link.
     */
    url?:                               string;
}

