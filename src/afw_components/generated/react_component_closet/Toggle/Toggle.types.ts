/**
 * Typescript interface definition for propTypes
 */
export interface IToggleProps {
    /**
     * disabled
     * Data Type: (boolean)
     * 
     * Specifies whether the Toggle component is disabled.
     */
    disabled?:                          boolean;
    /**
     * label
     * Data Type: (string)
     * 
     * The label for the Toggle component.
     */
    label?:                             string;
    /**
     * offText
     * Data Type: (string)
     * 
     * The text displayed when the Toggle component is off.
     */
    offText?:                           string;
    /**
     * onText
     * Data Type: (string)
     * 
     * The text displayed when the Toggle component is on.
     */
    onText?:                            string;
    /**
     * value
     * Data Type: (boolean)
     * 
     * The value for the Toggle component.
     */
    value?:                             boolean;
}

