/**
 * Typescript interface definition for propTypes
 */
export interface ISkeletonProps {
    /**
     * height
     * Data Type: (None)
     * 
     * The CSS height of the skeleton.
     */
    height?:                            any;
    /**
     * type
     * Data Type: (string)
     * 
     * The shape of the skeleton to display (line or circle).
     */
    type?:                              string;
    /**
     * width
     * Data Type: (None)
     * 
     * The CSS width of the skeleton.
     */
    width?:                             any;
}

